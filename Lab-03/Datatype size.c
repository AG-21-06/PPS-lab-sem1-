#include<stdio.h>
int main()
{
    printf("\n\n\nThe size of integer datatype is : %d \n", (int)sizeof(int));
    printf("The size of float datatype is : %d \n", (int)sizeof(float));
    printf("The size of double datatype is : %d \n", (int)sizeof(double));
    printf("The size of long integer datatype is : %d \n", (int)sizeof(long int));
    printf("The size of long double datatype is : %d \n\n\n", (int)sizeof(long double));
    return 0;
}